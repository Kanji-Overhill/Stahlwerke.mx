				<h1><b>Acerca de</b><br>NOSOTROS</h1>
		        <p>Somos una empresa dedicada a la ingeniería, diseño y fabricación de estructuras metálicas y sistemas de automatización a la medida.</p>
		        <p style="padding: 0;">Atendemos a una gran gama de industrias como la automotriz, energética, y aeronáutica.</p>
		        <p>Los equipos y estructuras que fabricamos son usados por compañías de diversos tamaños, así como el sector gubernamental y comercial.</p>
		        <h2>Ingeniería</h2>
		        <div class="img-right-min">
		        	<p>Stahl Werke cuenta con experiencia utilizando software de diseño tridimensional para diferentes tipos de industria. Durante la fase de ingeniería se analiza la viabilidad del producto en este tipo de software, este análisis se realiza tanto de planos del cliente como nuestros propios diseños, asegurando que el producto será entregado a tiempo y de acuerdo a las necesidades del cliente. <br><br>
		        	Contamos con tecnología de punta para el diseño, pruebas, simulaciones y fabricación de las estructuras necesarias para sus proyectos.</p>
		        	<img src="images/acerca2.jpg" alt="estructuras metalicas, naves industriales, equipos industriales">
		        </div>
		        <h2>Experiencia</h2>
		        <p>Nuestro equipo de trabajo cuenta con más de 20 años de experiencia por lo que ofrecemos valor agregado a las soluciones que se dan al cliente.</p>
		        <img src="images/acerca3.jpg" class="imagen-total" alt="estructuras metalicas, naves industriales, equipos industriales">
		        <h2>Equipo de trabajo</h2>
		        <p>Nuestro equipo de trabajo técnico-operativo se enfoca en escuchar y responder de manera efectiva y personalizada a nuestros clientes para ayudar a que las OEM, integradores y aliados sean exitosos y asegurar que sólo las mejores soluciones de acero sean diseñadas para sus procesos. Nuestro factor humano está integrado por un equipo interdisciplinario de ingenieros profesionales; además de esto, los dueños de la empresa se involucran directamente en los proyectos, garantizando un ambiente productivo y armonioso.</p>
		        <div class="img-left">
		        	<img src="images/acerca4.jpg" alt="estructuras metalicas, naves industriales, equipos industriales">
		        	<p>Buscamos abastecernos de lo mejor en el mercado nacional e internacional, buscando proveeduría de la más alta calidad mediante un sistema de calificación estricto sobre nuestros proveedores. Cuando el proyecto lo requiere, importamos los materiales o la maquinaria necesaria, contando con una base de datos de proveedores extranjeros.</p>
		        </div>
		        <h2>Forma de trabajo</h2>
		        <p>Stahl Werke trabaja con una red de integradores de sistemas, fabricantes de equipo original, distribuidores de equipos y usuarios de equipo, facilitando con esto la proveeduría de materiales, equipo especializado y el flujo de conocimiento necesario para lograr la mejor solución a su proceso.</p>
		        <img src="images/acerca5.jpg" class="imagen-total" alt="estructuras metalicas, naves industriales, equipos industriales">
		        <h2 style="font-size: 19px;">Tecnología y control de calidad</h2>
		        <p>La compañía utiliza la tecnología para aprovechar el conocimiento de su staff experimentando, utilizando software como AutoCAD, CATIA y Solid Works, así como maquinas CNC y soldadura especializada que permite un producto de mayor calidad.</p>
